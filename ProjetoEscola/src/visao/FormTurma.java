   public void preencherTablela(String Sql) {
       ArrayList dados = new ArrayList();
       String [] colunas = new String [] {"ID","Descri. Turma"};
       conex.conecxao();
       
       conex.executaSql(Sql);
       
       try{
           conex.rs.first();
           do {
               dados.add(new Object[]{conex.rs.getString("COD_TUR"),conex.rs.getString("DESCRI_TUR")});  
           }while(conex.rs.next());
           
       }catch(SQLException ex) {
           JOptionPane.showMessageDialog(rootPane, "Nenhum resultado encontrado na tabela.");
       }
       ModeloTabela modelo = new ModeloTabela(dados, colunas);
       
       jTableTurma.setModel(modelo);
       jTableTurma.getColumnModel().getColumn(0).setPreferredWidth(90);
       jTableTurma.getColumnModel().getColumn(0).setResizable(false);
       jTableTurma.getColumnModel().getColumn(1).setPreferredWidth(100);
       jTableTurma.getColumnModel().getColumn(1).setResizable(false);
       jTableTurma.getTableHeader().setReorderingAllowed(false);
       jTableTurma.setAutoResizeMode(jTableTurma.AUTO_RESIZE_OFF);
       jTableTurma.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
       conex.desconectar();
   } 
