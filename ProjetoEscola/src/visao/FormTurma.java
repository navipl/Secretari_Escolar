    private void jButtonSalvarTurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarTurmaActionPerformed
        if (jTextFieldCodigoTurma.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o CODIGO para continuar");
            jTextFieldCodigoTurma.requestFocus();
        }else if (jTextFieldNomeTurma.getText().isEmpty()) { 
            JOptionPane.showMessageDialog(null, "Informe a DESCRICAO da Turma");
            jTextFieldNomeTurma.requestFocus();
        }else 
 
        if (key == 1) {
           mod.setTur_cod(jTextFieldCodigoTurma.getText());
           mod.setTur_nome(jTextFieldNomeTurma.getText());
           control.Salvar(mod);
           
           jTextFieldCodigoTurma.setText("");
           jTextFieldNomeTurma.setText("");
           
           jTextFieldCodigoTurma.setEnabled(false);
           jTextFieldNomeTurma.setEnabled(false);

           
           jButtonSalvarTurma.setEnabled(false);
           jButtonCancelarTurma.setEnabled(false);
           jButtonPesquisaTurma.setEnabled(true);
           jTextFieldPesquisaTurma.setText("");
           jTextFieldPesquisaTurma.setEnabled(true);
           key = 0;
           preencherTablela("select * from turma order by DESCRI_TUR;");
       }
          
       if (key == 2){
           mod.setTur_cod(jTextFieldCodigoTurma.getText());
           mod.setTur_nome(jTextFieldNomeTurma.getText());
           control.Editar(mod);
           
           jTextFieldCodigoTurma.setText("");
           jTextFieldNomeTurma.setText("");
           
           jTextFieldCodigoTurma.setEnabled(false);
           jTextFieldNomeTurma.setEnabled(false);
          
           jButtonNovoTurma.setEnabled(true);
           jButtonCancelarTurma.setEnabled(false);
           
           key = 0;
           preencherTablela("select * from turma order by DESCRI_TUR;");
       }
       
    }//GEN-LAST:event_jButtonSalvarTurmaActionPerformed

