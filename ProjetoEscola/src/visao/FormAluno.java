    private void jTableAlunoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAlunoMouseClicked
        String nome_aluno = ""+jTableAluno.getValueAt(jTableAluno.getSelectedRow(), 1);
        conex.conecxao();
        conex.executaSql("select * from aluno where NOME_ALU = '" + nome_aluno + "';");
        try {
            conex.rs.first();
            
            jTextFieldCodigoAlu.setText(String.valueOf(conex.rs.getString("COD_ALU")));
            jTextFieldNomeAlu.setText(conex.rs.getString("NOME_ALU"));
            jTextFieldMaeAlu.setText(conex.rs.getString("MAE_ALU"));
            jTextFieldPaiAlu.setText(conex.rs.getString("PAI_ALU"));
            ConvData(conex.rs.getString("NASC_ALU"));
            jFormattedTextFieldNascAlu.setText(data_banco);
            jFormattedTextFieldFoneAlu.setText(conex.rs.getString("FONE_ALU"));
            jFormattedTextFieldCelularAlu.setText(conex.rs.getString("CEL__ALU"));
            
            jButtonEditarAluno.setEnabled(true);
            jButtonExcluirAluno.setEnabled(true);
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Erro ao Selecionar os Dados!" + ex);
        }
        
        conex.desconectar();
    }//GEN-LAST:event_jTableAlunoMouseClicked

