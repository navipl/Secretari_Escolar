   public void preencherTablela(String Sql) {
       ArrayList dados = new ArrayList();
       String [] colunas = new String [] {"ID","Nome Aluno","Nome Mãe","Nome Pai","Data de Nasc","Telefone","Celular"};
       conex.conecxao();
       
       conex.executaSql(Sql);
       
       try{
           conex.rs.first();
           do {
               ConvData(conex.rs.getString("NASC_ALU"));
               dados.add(new Object[]{conex.rs.getInt("COD_ALU"),conex.rs.getString("NOME_ALU"),conex.rs.getString("MAE_ALU"),conex.rs.getString("PAI_ALU"),data_banco,conex.rs.getString("FONE_ALU"),conex.rs.getString("CEL__ALU")});  
           }while(conex.rs.next());
           
       }catch(SQLException ex) {
           JOptionPane.showMessageDialog(rootPane, "Nenhum resultado encontrado na tabela.");
       }
       ModeloTabela modelo = new ModeloTabela(dados, colunas);
       
       jTableAluno.setModel(modelo);
       jTableAluno.getColumnModel().getColumn(0).setPreferredWidth(30);
       jTableAluno.getColumnModel().getColumn(0).setResizable(false);
       jTableAluno.getColumnModel().getColumn(1).setPreferredWidth(140);
       jTableAluno.getColumnModel().getColumn(1).setResizable(false);
       jTableAluno.getColumnModel().getColumn(2).setPreferredWidth(140);
       jTableAluno.getColumnModel().getColumn(2).setResizable(false);
       jTableAluno.getColumnModel().getColumn(3).setPreferredWidth(140);
       jTableAluno.getColumnModel().getColumn(3).setResizable(false);
       jTableAluno.getColumnModel().getColumn(4).setPreferredWidth(100);
       jTableAluno.getColumnModel().getColumn(4).setResizable(false);
       jTableAluno.getColumnModel().getColumn(5).setPreferredWidth(100);
       jTableAluno.getColumnModel().getColumn(5).setResizable(false);
       jTableAluno.getColumnModel().getColumn(6).setPreferredWidth(100);
       jTableAluno.getColumnModel().getColumn(6).setResizable(false);
       jTableAluno.getTableHeader().setReorderingAllowed(false);
       jTableAluno.setAutoResizeMode(jTableAluno.AUTO_RESIZE_OFF);
       jTableAluno.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
       conex.desconectar();
   } 
   
  
