    private void jTableProfessorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProfessorMouseClicked
        String nome_professor = ""+jTableProfessor.getValueAt(jTableProfessor.getSelectedRow(), 1);
        conex.conecxao();
        conex.executaSql("select * from professor where NOME_PROF = '" + nome_professor + "';");
        try {
            conex.rs.first();
            jTextFieldCodigoProf.setText(String.valueOf(conex.rs.getString("COD_PROF")));
            jTextFieldNomeProf.setText(conex.rs.getString("NOME_PROF"));
            jTextFieldFormacaoProf.setText(conex.rs.getString("FORMA_PROF"));
            jComboBoxTipoFormacaoProf.setSelectedItem(conex.rs.getString("TIPO_FORM"));
            jFormattedTextFieldFoneProf.setText(conex.rs.getString("FONE_PROF")); 
            jFormattedTextFieldCelularProf.setText(conex.rs.getString("CEL_PROF"));
            jButtonEditarProf.setEnabled(true);
            jButtonExcluirProf.setEnabled(true);
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Erro ao Selecionar os Dados!" + ex);
        }
        
        conex.desconectar();
    }//GEN-LAST:event_jTableProfessorMouseClicked

   public void preencherTablela(String Sql) {
       ArrayList dados = new ArrayList();
       String [] colunas = new String [] {"ID","Nome","Formacão","Tipo","Telefone","Celular"};
       conex.conecxao();
       
       conex.executaSql(Sql);
       
       try{
           conex.rs.first();
           do {
               dados.add(new Object[]{conex.rs.getInt("COD_PROF"),conex.rs.getString("NOME_PROF"),conex.rs.getString("FORMA_PROF"),conex.rs.getString("TIPO_FORM"),conex.rs.getString("FONE_PROF"),conex.rs.getString("CEL_PROF")});  
           }while(conex.rs.next());
           
       }catch(SQLException ex) {
           JOptionPane.showMessageDialog(rootPane, "Nenhum resultado encontrado na tabela.");
       }
       ModeloTabela modelo = new ModeloTabela(dados, colunas);
       
       jTableProfessor.setModel(modelo);
       jTableProfessor.getColumnModel().getColumn(0).setPreferredWidth(30);
       jTableProfessor.getColumnModel().getColumn(0).setResizable(false);
       jTableProfessor.getColumnModel().getColumn(1).setPreferredWidth(150);
       jTableProfessor.getColumnModel().getColumn(1).setResizable(false);
       jTableProfessor.getColumnModel().getColumn(2).setPreferredWidth(150);
       jTableProfessor.getColumnModel().getColumn(2).setResizable(false);
       jTableProfessor.getColumnModel().getColumn(3).setPreferredWidth(150);
       jTableProfessor.getColumnModel().getColumn(3).setResizable(false);
       jTableProfessor.getColumnModel().getColumn(4).setPreferredWidth(100);
       jTableProfessor.getColumnModel().getColumn(4).setResizable(false);
       jTableProfessor.getColumnModel().getColumn(5).setPreferredWidth(100);
       jTableProfessor.getColumnModel().getColumn(5).setResizable(false);
       jTableProfessor.getTableHeader().setReorderingAllowed(false);
       jTableProfessor.setAutoResizeMode(jTableProfessor.AUTO_RESIZE_OFF);
       jTableProfessor.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
       conex.desconectar();
   } 
