    private void jButtonSalvarProfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarProfActionPerformed
        if (jTextFieldNomeProf.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o NOME para continuar");
            jTextFieldNomeProf.requestFocus();
        }else if (jTextFieldFormacaoProf.getText().isEmpty()) { 
            JOptionPane.showMessageDialog(null, "Informe a FORMAÇAO para continuar");
            jTextFieldNomeProf.requestFocus();
        }else if (jFormattedTextFieldCelularProf.getText().isEmpty()) { 
            JOptionPane.showMessageDialog(null, "Informe o CELULAR para continuar");
            jTextFieldNomeProf.requestFocus();
        } else
        if (key == 1) {
           mod.setProf_nome(jTextFieldNomeProf.getText());
           mod.setProf_form(jTextFieldFormacaoProf.getText());
           mod.setTipo_form((String) jComboBoxTipoFormacaoProf.getSelectedItem());
           mod.setProf_cel(jFormattedTextFieldCelularProf.getText());
           mod.setProf_tel(jFormattedTextFieldFoneProf.getText());
           control.Salvar(mod);
           jTextFieldNomeProf.setText("");
           jTextFieldFormacaoProf.setText("");
           jFormattedTextFieldCelularProf.setText("");
           jFormattedTextFieldFoneProf.setText("");
           jTextFieldNomeProf.setEnabled(false);
           jTextFieldFormacaoProf.setEnabled(false);
           jComboBoxTipoFormacaoProf.setEnabled(false);
           jFormattedTextFieldCelularProf.setEnabled(false);
           jFormattedTextFieldFoneProf.setEnabled(false);
           jButtonSalvarProf.setEnabled(false);
           jButtonCancelarProf.setEnabled(false);
           jButtonPesquisaProf.setEnabled(true);
           jTextFieldPesquisaProf.setText("");
           jTextFieldPesquisaProf.setEnabled(true);
           key = 0;
           preencherTablela("select * from professor order by NOME_PROF;");
       } else
          
       if (key == 2){
           mod.setProf_cod(Integer.parseInt(jTextFieldCodigoProf.getText()));
           mod.setProf_nome(jTextFieldNomeProf.getText());
           mod.setProf_form(jTextFieldFormacaoProf.getText());
           mod.setTipo_form((String) jComboBoxTipoFormacaoProf.getSelectedItem());
           mod.setProf_cel(jFormattedTextFieldCelularProf.getText());
           mod.setProf_tel(jFormattedTextFieldFoneProf.getText());
           control.Editar(mod);
           jTextFieldNomeProf.setText("");
           jTextFieldFormacaoProf.setText("");
           jFormattedTextFieldCelularProf.setText("");
           jFormattedTextFieldFoneProf.setText("");
           jTextFieldCodigoProf.setText("");
           jTextFieldNomeProf.setEnabled(false);
           jTextFieldFormacaoProf.setEnabled(false);
           jComboBoxTipoFormacaoProf.setEnabled(false);
           jFormattedTextFieldCelularProf.setEnabled(false);
           jFormattedTextFieldFoneProf.setEnabled(false);
           jButtonNovoProf.setEnabled(true);
           jButtonCancelarProf.setEnabled(false);
           key = 0;
           preencherTablela("select * from professor order by NOME_PROF;");
       }
       
    }//GEN-LAST:event_jButtonSalvarProfActionPerformed

